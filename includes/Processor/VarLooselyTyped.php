namespace ApiOpenStudio\Processor;

use ApiOpenStudio\Core;

/**
 * Class VarLooselyTyped
 *
 * Processor class to define a loosely typed variable.
 */
class VarLooselyTyped extends Core\ProcessorEntity
{
    /**
     * {@inheritDoc}
     *
     * @var array Details of the processor.
     */
    protected array $details = [
        'name' => 'Loosely typed',
        'machineName' => 'var_loosely_typed',
        // phpcs:ignore
        'description' => 'A loosely typed variable (i.e. not strictly typed). There are no restrictions on the input, however the result will have type calculated.',
        'menu' => 'Primitive',
        'input' => [
            'value' => [
                'description' => 'The value of the variable.',
                'cardinality' => [1, 1],
                'literalAllowed' => true,
                'limitProcessors' => [],
                'limitTypes' => [],
                'limitValues' => [],
                'default' => '',
            ],
        ],
    ];

    /**
     * {@inheritDoc}
     *
     * @return Core\DataContainer Result of the processor.
     *
     * @throws Core\ApiException Exception if invalid result.
     */
    public function process(): Core\DataContainer
    {
        parent::process();

        return $this->val('value');
    }
}
